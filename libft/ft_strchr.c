#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;

	i = 0;
	if (s)
	{
		while (s[i] && c != s[i])
			i++;
		if (c == s[i])
			return ((char*)s + i);
	}
	return (NULL);
}
